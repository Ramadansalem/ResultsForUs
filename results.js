function showResults() {
  const studentId = document.getElementById("studentId").value;
  const subjectId = document.getElementById("subjectId").value;
  const student = data.students.find((s) => s.id === studentId);
  const subject = student ? student.subjects[subjectId] : null;

  if (student && subject) {
    document.getElementById("result").innerHTML = `
            <h2>اسم الطالب: ${student.name}</h2>
            <h3>المادة: ${subject.name}</h3>
            <p>درجة السلوك: ${subject.behavior}</p>
            <p>درجة الواجبات: ${subject.homework}</p>
            <p>درجة الكراسة: ${subject.notebook}</p>
            <p>درجة الحضور والغياب: ${subject.attendance}</p>
            <p>درجة التطبيقات الشهرية: ${subject.monthlyApplications}</p>
            <p>الدرجة الكلية لاعمال الفصل الاول: ${subject.total}</p>
            <p>درجة امتحان نهاية الفصل: ${subject.finalExam}</p>
            <p>درجة امتحان العملي: ${subject.practicalExam}</p>
        `;
  } else {
    document.getElementById("result").innerHTML = "<p>النتيجة غير متوفرة</p>";
  }
}
